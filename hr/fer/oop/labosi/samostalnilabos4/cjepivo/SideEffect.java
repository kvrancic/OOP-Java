package hr.fer.oop.labosi.samostalnilabos4.cjepivo;

public class SideEffect {
    private String name;
    private int code;

    public SideEffect(String name, int code) {
        this.name = name;
        this.code = code;
    }

    public String getName() {
        return name;
    }

    public int getCode() {
        return code;
    }
}
